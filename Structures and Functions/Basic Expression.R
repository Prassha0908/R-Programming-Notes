## ASSIGNMENT EXPERSSION 


# " = " : assigns right side value to left side operand
a = 10


#  " <- "  : assigns right side value to left side operand
# assigns value to local
a <- 9


# " -> "  : assigns left side value to right side operand
8 -> a


# " <<- " : assigns right side value to left side operand
# assigns value to parent/global
a <<- 7


# " ->> " : assigns left side value to right side operand
6 ->> a



